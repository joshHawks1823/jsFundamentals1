function hi(name) {
  console.log(`Hi ${name}!`);
}

hi("Josh");

function pet(animal) {
  console.log(`My puppy's name is ${animal}.`);
}

pet("Sumo");
pet("pete");
pet("boomer");

function name(first, last) {
  let wholeName = first + " " + last;
  console.log(`Hello, name is ${wholeName}`);
}

name("Josh", "Hawkins");
